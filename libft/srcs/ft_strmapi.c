#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	i;
	size_t	len;

	i = 0;
	if (!s)
		return (NULL);
	len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (str == NULL)
		return (NULL);
	if (s != NULL && f != NULL)
	{
		while (i < len)
		{
			str[i] = (*f)(i, s[i]);
			i++;
		}
	}
	str[i] = '\0';
	return (str);
}
